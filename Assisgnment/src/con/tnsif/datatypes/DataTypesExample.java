package con.tnsif.datatypes;

public class DataTypesExample {

	    public static void main(String[] args) {

	        // Integer types
	        byte byteVar = 100;          // 1 byte
	        short shortVar = 20000;      // 2 bytes
	        int intVar = 500000;         // 4 bytes
	        long longVar = 15000000000L; // 8 bytes (use 'L' at the end)

	        // Floating-point types
	        float floatVar = 5.75f;      // 4 bytes (use 'f' at the end)
	        double doubleVar = 19.99;    // 8 bytes

	        // Character type
	        char charVar = 'A';          // 2 bytes

	        // Boolean type
	        boolean boolVar = true;      // 1 bit (true/false)

	        // Displaying all values
	        System.out.println("=== Data Types in Java ===");
	        System.out.println("byte value: " + byteVar);
	        System.out.println("short value: " + shortVar);
	        System.out.println("int value: " + intVar);
	        System.out.println("long value: " + longVar);
	        System.out.println("float value: " + floatVar);
	        System.out.println("double value: " + doubleVar);
	        System.out.println("char value: " + charVar);
	        System.out.println("boolean value: " + boolVar);
	    }
	}



